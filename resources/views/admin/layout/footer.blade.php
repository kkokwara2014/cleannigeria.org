<!--calculating certificate validity period-->
<script>
    $(function () {

        $("#issuedon").on("change", function(e){
            e.preventDefault();

            var issued_date=$("#issuedon").val();
            var newdate=new Date(issued_date);
            var day=newdate.getDate();
            var month=newdate.getMonth();
            month+=1;
            var year=newdate.getFullYear();
            var newyear=newdate.getFullYear() + 4;

            var valid_till=(month+"/"+day+"/"+newyear);
            
            $("#validityperiod").val(valid_till);

        });
});
</script>

