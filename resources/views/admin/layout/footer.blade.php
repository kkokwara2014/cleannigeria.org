{{-- calculating number of leave days based on choosen dates --}}

<script>
    $(function () {
        $("#datepicker, #datepicker1").on("change", function(e){
            e.preventDefault();

            var starting=new Date($("#datepicker").val());
            var ending=new Date($("#datepicker1").val());
            var millisBetween=starting.getTime()-ending.getTime();
            var days=millisBetween/(1000*3600*24);

            var numofdays = Math.round(Math.abs(days));

            //set the value of number of days 
            $("#numofdays").val(numofdays);
        })
});
</script>

