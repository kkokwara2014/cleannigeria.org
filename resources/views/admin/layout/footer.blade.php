                        radioBtnsemiannually.appendTo('#maintcycle');
                        var radioBtnannually = $('<input type="radio" name="nextmaintperiod" value="'+ 12 +'"/> Annually [Every year or every 1000hrs]<br>');
                        radioBtnannually.appendTo('#maintcycle');
                    });
                    
                }
            });
        }else{
            $('#maintcycle').hide();
        }
    });

});

</script>

